<h1>F.A.Q.</h1>
<h2>Haricot Magique, c'est quoi ?</h2>
<p>Haricot Magique est une simulation agricole par contrainte.</p>
<h2>Haricot Magique, pour qui ?</h2>
<p>Haricot Magique est avant tout destiné à des agriculteurs désirant prévoir la croissance de leurs plantes.<br />
Cette application web est un formidable outils décisionnel vous permetttant de faire les bons choix de plantes en fonction des types de sol que vous exploitez.</p>
<h2>Combien ça coûte ?</h2>
<p>L'inscription sur Haricot Magique est totalement gratuite.</p>
