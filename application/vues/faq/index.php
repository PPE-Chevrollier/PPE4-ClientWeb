<h1>F.A.Q.</h1>
<h2>Q : ChevLoc, c'est quoi ?</h2>
<p>R : ChevLoc est une plate-forme d'échange autour du logement proposée par le Lycée Chevrollier.</p>
<h2>Q : ChevLoc, pour qui ?</h2>
<p>R : ChevLoc est dédié aux étudiants du Lycée Chevrollier qui recherchent un logement sur Angers ou dans ses environs.<br />
Cette application web met en relation des étudiants sur le point de quitter leur logement avec de nouveaux arrivants qui en cherchent.</p>
<h2>Q : Combien ça coûte ?</h2>
<p>R : L'inscription sur ChevLoc est organisée par le Lycée Chevrollier et l'utilisation de la plate-forme est totalement gratuite.</p>
<h2>Q : Qui se cache derrière ChevLoc ?</h2>
<p>ChevLoc a été créé par la jeune société angevine PPE-Chevrollier à l'initiative du Lycée Chevrollier.<br />
Pour plus d'informations à notre sujet, consultez la rubrique « <a href="/a-propos">A propos</a> ».</p>